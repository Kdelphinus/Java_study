package variable;

public class Var3 {
    public static void main(String[] args) {
        // 개발자가 직접 넣은 고정된 값을 리터럴이라고 한다.
        int a = 100; // 정수
        double b = 10.5; // 실수
        boolean c = true; // 참 혹은 거짓
        char d = 'A';  // 문자 하나
        String e = "Hello Java"; // 문장

        System.out.println(a);
        System.out.println(b);
        System.out.println(c);
        System.out.println(d);
        System.out.println(e);
    }
}
