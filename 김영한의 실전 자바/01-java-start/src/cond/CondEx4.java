package cond;

public class CondEx4 {
    public static void main(String[] args) {
        double rate = 7.9;

        if (rate <= 9) {
            System.out.println("9 rate");
        }
        if (rate <= 8) {
            System.out.println("8 rate");
        }
        if (rate <= 7) {
            System.out.println("7 rate");
        }
        if (rate <= 6) {
            System.out.println("6 rate");
        }
    }
}
